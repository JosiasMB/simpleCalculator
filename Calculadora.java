public class Calculadora {

    public int sumar(int a, int b){
        return a + b;
    }

    public int sumar(int a, int b, int c){
        return a + b + c;
    }

    public int sumar(int a, int b, int c, int d){
        return a + b + c + d;
    }

    public int restar(int a, int b){
        return a - b;
    }
    public int restar(int a, int b, int c){
        return a - b - c;
    }
    public int restar(int a, int b, int c, int d){
        return a - b - c - d;
    }

    public int multiplicar(int a, int b){
        return a * b;
    }
    public int multiplicar(int a, int b, int c){
        return a * b * c;
    }
    public int multiplicar(int a, int b, int c, int d){
        return a * b * c * d;
    }

    public double dividir (double a, double b){
        return a / b;
    }
    public double dividir (double a, double b, double c){
        return a / b / c;
    }
    public double dividir (double a, double b, double c, double d){
        return a / b / c / d;
    }
}

